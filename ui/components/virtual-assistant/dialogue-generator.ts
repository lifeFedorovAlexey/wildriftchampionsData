const recentLines: string[] = [];
const RECENT_LIMIT = 40;

function randomItem<T>(items: readonly T[]) {
  return items[Math.floor(Math.random() * items.length)];
}

function normalizeLine(line: string) {
  return line
    .replace(/\s+/g, " ")
    .replace(/\s+([,.!?;:])/g, "$1")
    .trim();
}

function chooseUnique(candidates: readonly string[]) {
  const normalized = [...new Set(candidates.map(normalizeLine))];
  const fresh = normalized.filter((line) => !recentLines.includes(line));
  const selected = randomItem(fresh.length ? fresh : normalized);

  recentLines.push(selected);
  if (recentLines.length > RECENT_LIMIT) recentLines.shift();
  return selected;
}

function combine(groups: ReadonlyArray<readonly string[]>) {
  const candidates: string[] = [];
  const build = (index: number, parts: string[]) => {
    if (index >= groups.length) {
      candidates.push(parts.join(" "));
      return;
    }
    for (const part of groups[index]) build(index + 1, [...parts, part]);
  };
  build(0, []);
  return chooseUnique(candidates);
}

function percent(value: number | null) {
  return value == null ? "—" : `${value.toFixed(1)}%`;
}

